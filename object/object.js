let car = {
    brand : "Toyota",
    model : "Corolla", 
    year : 2020,
};

console.log(car.brand); // Accessing object properties using dot notation
console.log(car["model"]); // Accessing object properties using bracket notation
console.log(car.model); // Accessing object properties using dot notation again
console.log(car.year); // Accessing object properties using bracket notation again